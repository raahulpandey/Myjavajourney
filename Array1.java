import java.util.*;
class Array1 {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int size=sc.nextInt();
        int num[]=new int[size];
        num[0]=10;
        num[1]=11;
        for(int i=0;i<=size;i++){
            System.out.println(num[i]);
        }      
    
    }
}